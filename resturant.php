As you walk a few steps from Glenary's towards the Chowrasta Mall, Shangri-La Restaurant will be on your right. It is actually part of a two storied old heritage building which is run as a hotel with only three rooms upstairs, and the restaurant is in the ground floor. As you walk in and cross the small reception cum lounge area, you will know that you are getting into a nice restaurant with an wooden decor, arches and great interiors. This in my view is one of the best restaurants of Darjeeling now considering the ambience, food and friendly service. 
 
