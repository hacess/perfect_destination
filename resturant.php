						<h3>Keventer's </h3><h5 align="center" style="font-size:20px">(English Breakfast & Snacks) </h5>
				</div>
					<br>
For us, a Darjeeling vacation is never complete without a breakfast at the Keventer's, and this is the reason why this restaurant appears on my list of best eateries. If you like to go for traditional English breakfast with items like great sandwiches, chicken salami & meat loafs, or pork sausages with eggs followed by a pot of perfect Darjeeling tea, this is the place to go. 
 
Located right at the beginning of Nehru Road that leads towards the Mall, this strange looking narrow building has two floors. The ground floor is a bakery. Climb up the stairs to enter the small cozy indoor area or just walk across to come out to the open terrace that has rot iron tables and chairs with lovely views all around. This is where I love to sit and have my breakfast while enjoying Kanchenjunga and the Nehru Road activities below. A sip at the tea while reading a newspaper and looking up at times to see how the clouds play at the mountains, is just something that I long for ever. If not for anything else, it can be just Keventer's why I can be in Darjeeling once again. 
