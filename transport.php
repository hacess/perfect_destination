<h5 align="center" id="font"> Darjeeling via NJP</h5>
<br>
Take a train up to NJP (New Jalpaiguri). Darjeeling is only 88kms from here. You will get ample taxis and shared jeeps from NJP to reach Darjeeling in about 3.5 hours. Few long distance trains pass through Siliguri as well. You will also get plenty of shared jeeps, taxis and buses at Siliguri (takes about 3 hours to Darjeeling). You can even take a toy train ride from either NJP or Siliguri through scenic mountain route that takes about 7.5 hrs to reach Darjeeling. The toy train track runs parallel and next to the Hill Cart Road for most part. Check Toy Train Service for schedule and running status.
